package service.aplication.dto.bolao;

public class PerfilDTO {
	
	private String totalTrofeus;
	private String totalBolaos;
	private String totalConvidados;
	private String totalPontos;
	public String getTotalTrofeus() {
		return totalTrofeus;
	}
	public void setTotalTrofeus(String totalTrofeus) {
		this.totalTrofeus = totalTrofeus;
	}
	public String getTotalBolaos() {
		return totalBolaos;
	}
	public void setTotalBolaos(String totalBolaos) {
		this.totalBolaos = totalBolaos;
	}
	public String getTotalConvidados() {
		return totalConvidados;
	}
	public void setTotalConvidados(String totalConvidados) {
		this.totalConvidados = totalConvidados;
	}
	public String getTotalPontos() {
		return totalPontos;
	}
	public void setTotalPontos(String totalPontos) {
		this.totalPontos = totalPontos;
	}
	
	
	
	

}
